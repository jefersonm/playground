package com.github.jefersonm.sandbox.scala.scalatutorial.lesson2

object NumberAreObject {

	def main(args: Array[String]) {
		println(1 + 2 * 3 / 3)
		//it's the same as the above, but as everything in Scala is object, the number is an object too,
		//but you must put brackets around the numbers to scala don't see it as a double 
		println((1).+(((2).*(3))./(3)))
	}

}